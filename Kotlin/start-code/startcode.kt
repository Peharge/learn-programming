fun main() {
    // Begrüßung
    println("Willkommen zu meinem Kotlin-Programm!")

    // Nutzereingabe
    print("Bitte geben Sie Ihren Namen ein: ")
    val name = readLine() // Liest eine Eingabe von der Konsole

    // Begrüßung mit dem Namen
    println("Hallo, $name! Schön, dass Sie hier sind.")

    // Programm beenden
    println("Das Programm wurde beendet.")
}
